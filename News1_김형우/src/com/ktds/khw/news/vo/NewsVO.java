package com.ktds.khw.news.vo;

public class NewsVO {

	private String newsHeadLine;
	private String newsPress;
	private String reporter;

	public String getNewsHeadLine() {
		return newsHeadLine;
	}

	public void setNewsHeadLine(String newsList) {
		this.newsHeadLine = newsList;
	}

	public String getNewsPress() {
		return newsPress;
	}

	public void setNewsPress(String newsPress) {
		this.newsPress = newsPress;
	}

	public String getReporter() {
		return reporter;
	}

	public void setReporter(String reporter) {
		this.reporter = reporter;
	}

}
