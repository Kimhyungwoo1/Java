package com.ktds.khw.vo;

public class HumanVO {
	
	private int humanMoney;
	private int humanCoke;
	private int humanCider;
	private int humanFanta;
	
	public int getHumanMoney() {
		return humanMoney;
	}

	public void setHumanMoney(int humanMoney) {
		this.humanMoney = humanMoney;
	}

	public int getHumanCoke() {
		return humanCoke;
	}

	public void setHumanCoke(int humanCoke) {
		this.humanCoke = humanCoke;
	}

	public int getHumanCider() {
		return humanCider;
	}

	public void setHumanCider(int humanCider) {
		this.humanCider = humanCider;
	}

	public int getHumanFanta() {
		return humanFanta;
	}

	public void setHumanFanta(int humanFanta) {
		this.humanFanta = humanFanta;
	}
	
}
